package projetoUML;

public class NavegadorInternet {
    private boolean exibirPagina;
    private boolean adicionarNovaAba;
    private boolean atualizarPagina;

    public boolean isExibirPagina() {
        return exibirPagina;
    }

    public void setExibirPagina(boolean exibirPagina) {
        this.exibirPagina = exibirPagina;
    }

    public boolean isAdicionarNovaAba() {
        return adicionarNovaAba;
    }

    public void setAdicionarNovaAba(boolean adicionarNovaAba) {
        this.adicionarNovaAba = adicionarNovaAba;
    }

    public boolean isAtualizarPagina() {
        return atualizarPagina;
    }

    public void setAtualizarPagina(boolean atualizarPagina) {
        this.atualizarPagina = atualizarPagina;
    }

}
